#include <stdio.h>
float farenheight;
int main()
{
    float celcius, farenheight;
    printf("Enter the value in celcius\n");
    scanf("%f", &celcius);

    printf("The convergence from celcius to farenheight is %f", farenheight);
    return 0;
}
float farenheight
{
    farenheight = (celcius * 9 / 5) + 32;
    return farenheight;
}
